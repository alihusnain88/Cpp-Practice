// Basic Typecasting

#include <iostream>
using namespace std;

int main()
{
    // Typecasting****************

    int a = 2;
    cout << "The integer value of a is :" << a       <<endl;
    cout << "The float value of a is :" << (float)a  <<endl;

    float b = 10.25;
    cout << "The float value of b is :" << b         <<endl; // b = 10.25
    cout << "The integer value of b is :" << (int)b  <<endl; // b = 10    

    return 0;
}
